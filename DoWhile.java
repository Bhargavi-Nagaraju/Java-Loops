// print 1 to 10 numbers
public class DoWhile{
public static void main (String[]args){
// Step 1: Initialize the variable i with 1
int i = 1;
// Step 2: Start the do-while loop
do{
// Step 3: Print the current value of i
System.out.print(i + " ");
// Step 4: Increment i by 1
i++;
}while (i<=10);
// Step 5: Continue the loop as long as i is less than or equal to 10

}
}